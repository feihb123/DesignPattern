    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        int row = jTable1.getSelectedRow();
        if(repair.isSelected()){
                jTable1.setValueAt("空闲",row,2);
                new Room(new Free()).handle();
           }
           if(appoint.isSelected()){
               jTable1.setValueAt("已预订",row,2);
               new Room(new Appointed()).handle();
           }
           if(checkin.isSelected()){
                jTable1.setValueAt("已入住",row,2);
               new Room(new Checked()).handle();
           }
           if(forRepair.isSelected()){
                 jTable1.setValueAt("维修",row,2);
                new Room(new Repair()).handle();
           }
           if(cancel.isSelected()){
                jTable1.setValueAt("空闲",row,2);
                new Room(new Free()).handle();
           }
           if(swap.isSelected()){
                jTable1.setValueAt("空闲",row,2);
                new Room(new Free()).handle();
           }
           if(checkout.isSelected()){
                jTable1.setValueAt("空脏房",row,2);
                new Room(new Dirty()).handle();
           }
           
    }//GEN-LAST:event_jButton7ActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int row = jTable1.getSelectedRow();
        String state = jTable1.getValueAt(row,2).toString();
        switch(state){
            case "空闲":new Room(new Free()).handle();break;
            case "已入住":new Room(new Checked()).handle();break;
            case "维修":new Room(new Repair()).handle();break;
            case "空脏房":new Room(new Dirty()).handle();break;
            case "已预订":new Room(new Appointed()).handle();break;
        }
    }//GEN-LAST:event_jTable1MouseClicked

