     interface State {
        public void handle();
    }

    class Room implements State{
        State state;

        public Room(State state) {
            this.state = state;
        }
        public Room() {
            
        }
        
        public State getState() {
            return state;
        }

        public void setState(State state) {
            this.state = state;
        }

        @Override
        public void handle() {
            this.state.handle();
        }
        
        
        
    }

